class 今日進貨肉品:
    #肉品的建構子,及三個屬性
    def __init__(self,名稱,重量,價格) :
        self._名稱=名稱
        self._重量=重量
        self._價格=價格
#建立今日進貨肉品的物件
肉品1=今日進貨肉品("牛肉", 30, 150)
肉品2=今日進貨肉品("豬肉", 40, 90)
肉品3=今日進貨肉品("羊肉", 20, 100)
#顯示今日進貨肉品的名稱,重量,總價
print("今日進貨",肉品1._名稱)
print("重量=",肉品1._重量,"KG")
print("價格=",肉品1._重量*肉品1._價格,"元\n")

print("今日進貨",肉品2._名稱)
print("重量=",肉品2._重量,"KG")
print("價格=",肉品2._重量*肉品2._價格,"元\n")

print("今日進貨",肉品3._名稱)
print("重量=",肉品3._重量,"KG")
print("價格=",肉品3._重量*肉品3._價格,"元\n")

